    private void degistirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_degistirActionPerformed
        // TODO add your handling code here:
        EntityManager em = emf.createEntityManager();
        Query q1 = em.createQuery("select m from Malzeme m");
        List<Malzeme> malzemeler = q1.getResultList();
        for (Malzeme malzeme : malzemeler) {
            if (malzeme.getKodu().equals(malzemeList.getSelectedValue())) {
                malzeme.setBiRiMfiYat(this.birimFiyatı.getText());
                malzeme.setAdi(this.malzemeİsmi.getText());
                malzeme.setMiKtar(this.miktar.getText());
                malzeme.setNakliYefiYat(this.nakliyeFiyatı.getText());
                malzeme.setCiNsi((String) birimCinsi.getSelectedItem());
                em.getTransaction().begin();
                em.getTransaction().commit();
                em.close();

            }

            for (int k = 0; k < projeYönetim.dtm2.getRowCount(); k++) {
                if (projeYönetim.dtm2.getValueAt(k, 6).equals(malzemeList.getSelectedValue())) {
                    projeYönetim.dtm2.setValueAt(malzemeİsmi.getText(), k, 0);
                    projeYönetim.dtm2.setValueAt(this.nakliyeFiyatı.getText(), k, 4);
                    projeYönetim.dtm2.setValueAt(birimFiyatı.getText(), k, 3);
                    projeYönetim.dtm2.setValueAt(birimCinsi.getSelectedItem(), k, 2);
                    projeYönetim.dtm2.setValueAt(this.miktar.getText(), k, 1);

                }
            }

        }
    }//GEN-LAST:event_degistirActionPerformed

