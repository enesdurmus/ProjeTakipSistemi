    public malzemeDegistir(String kullanıcıId) {
        initComponents();
        this.kullanıcıId = kullanıcıId;
        dlm.removeAllElements();
        EntityManager em = emf.createEntityManager();
        Query q1 = em.createQuery("select p from Proje p where p.kullaniciid='" + kullanıcıId + "'");
        Query q2 = em.createQuery("select m from Malzeme m");
        List<Malzeme> malzemeler = q2.getResultList();
        List<Proje> projeler = q1.getResultList();
        for (Proje proje : projeler) {
            for (Malzeme malzeme : malzemeler) {
                if (malzeme.getKullanilacagiprojekodu().equals(proje.getProjekodu())) {
                    dlm.addElement(malzeme.getKodu());
                }
            }
        }
        malzemeList.setModel(dlm);
    }

