    private void personelEkleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_personelEkleActionPerformed
        // TODO add your handling code here:
        EntityManager em = emf.createEntityManager();
        Query q1 = em.createQuery("select p from Proje p where p.kullaniciid='" + kullanıcıId + "'");
        Query q2 = em.createQuery("select p from Personel p");
        List<Personel> personeller = q2.getResultList();
        List<Proje> projeler = q1.getResultList();
        String cinsiyet;
        boolean varmı = true;
        boolean projeVarmı = false;
        boolean girisTarihiDogrumu = true;
        int[] girisTarih = new int[3];
        String girisTarihi = null;
        girisTarih[0] = (Integer.valueOf(girisGun.getSelectedIndex()) + 1);
        girisTarih[1] = (Integer.valueOf(girisAy.getSelectedIndex()) + 1);
        girisTarih[2] = (Integer.valueOf(girisYıl.getSelectedIndex()) + 2018);
        girisTarihi = girisTarih[0] + "," + girisTarih[1] + "," + girisTarih[2];
        for (Proje proje : projeler) {
            if (jComboBox1.getSelectedItem().equals(proje.getProjekodu())) {
                String bas[] = proje.getBaslangictarih().split(",");
                String bit[] = proje.getBitistarih().split(",");
                if (Integer.valueOf(bas[2]) > Integer.valueOf((String) girisYıl.getSelectedItem()) || Integer.valueOf(bit[2]) < Integer.valueOf((String) girisYıl.getSelectedItem())) {
                    girisTarihiDogrumu = false;
                }
            }
        }

        if (!desenKontrol("[^a-zA-Z]", personelAdı.getText())) {
            if (!desenKontrol("[^a-zA-Z]", personelSoyadı.getText())) {
                if (!desenKontrol("[^0-9]", personelMaas.getText())) {
                    for (Proje p : projeler) {
                        if (p.getProjekodu().equalsIgnoreCase((String) jComboBox1.getSelectedItem())) {
                            projeVarmı = true;
                        }
                    }

                    for (Personel p : personeller) {
                        if (p.getTckimlik().equalsIgnoreCase(this.personelTcNo.getText())) {
                            varmı = false;
                        }
                    }

                    if (!(personelAdı.getText().equalsIgnoreCase("") || personelMaas.getText().equalsIgnoreCase("") || (personelOzelligi.getSelectedIndex() == -1) || (personelRutbesi.getSelectedIndex() == -1) || personelSoyadı.getText().equalsIgnoreCase("") || personelTcNo.getText().equalsIgnoreCase("") || personelMaas.getText().equalsIgnoreCase(""))) {
                        if (personelTcNo.getText().length() == 11) {
                            if (projeVarmı) {
                                if (varmı) {
                                    if (girisTarihiDogrumu) {
                                        if (cinsiyetKadın.isSelected()) {
                                            cinsiyet = cinsiyetKadın.getText();
                                        } else {
                                            cinsiyet = cinsiyetErkek.getText();
                                        }

                                        File f = new File("C:/Users/X550V/Desktop", "Personel.dat");
                                        if (!f.exists()) {
                                            try {
                                                f.createNewFile();
                                            } catch (IOException ex) {
                                                Logger.getLogger(projeYönetim.class.getName()).log(Level.SEVERE, null, ex);
                                            }
                                        }
                                        try {
                                            Personel personel = new Personel(personelTcNo.getText(), personelAdı.getText(), personelSoyadı.getText(), personelOzelligi.getSelectedItem().toString(), personelRutbesi.getSelectedItem().toString(), (String) jComboBox1.getSelectedItem(), girisTarihi, personelMaas.getText(), cinsiyet);
                                            ObjectOutput oos = new ObjectOutputStream(new BufferedOutputStream(new FileOutputStream("/C:/Users/X550V/Desktop/Personel.dat")));
                                            oos.writeObject(personel);
                                            oos.close();
                                        } catch (IOException ex) {
                                            Logger.getLogger(projeGiris.class.getName()).log(Level.SEVERE, null, ex);
                                        }
                                        Personel yeniPersonel = new Personel(personelTcNo.getText(), personelAdı.getText(), personelSoyadı.getText(), personelOzelligi.getSelectedItem().toString(), personelRutbesi.getSelectedItem().toString(), (String) jComboBox1.getSelectedItem(), girisTarihi, personelMaas.getText(), cinsiyet);
                                        em.getTransaction().begin();
                                        em.persist(yeniPersonel);
                                        em.getTransaction().commit();
                                        dtm.setColumnIdentifiers(new String[]{"Personel Adı", "Soyadı", "Özelliği", "Rütbesi", "Çalışacağı Proje", "Personel TC", "Giris Tarihi"});
                                        dtm.addRow(new String[]{yeniPersonel.getAdi(), yeniPersonel.getSoyadi(), yeniPersonel.getOzelligi(), yeniPersonel.getRutbesi(), yeniPersonel.getCalisacagiproje(), yeniPersonel.getTckimlik(), girisTarihi});
                                        personelTablosu.setModel(dtm);
                                        em.close();

                                    } else {
                                        JOptionPane.showMessageDialog(rootPane, "Tarihleri Kontrol Ediniz ...");
                                    }
                                } else {
                                    JOptionPane.showMessageDialog(rootPane, "Bu Tc'ye Sahip Personelimiz Zaten Mevcuttur ...");
                                }
                            } else {
                                JOptionPane.showMessageDialog(rootPane, "Böyle Bir Proje Yoktur ...");
                            }
                        } else {
                            JOptionPane.showMessageDialog(rootPane, "TC'yi Doğru Girdiğinizden Emin Olun ...");
                        }
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "Bütün Değerleri Girdiğinizden Emin Olun ...");
                    }
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Personel Maaşına Sadece Rakam Girdiğinizden Emin Olun ...");
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Personel Soyadı Özel Karakter Ve Rakam İçeremez ...");
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Personel Adı Özel Karakter Ve Rakam İçeremez ...");
        }
    }//GEN-LAST:event_personelEkleActionPerformed

