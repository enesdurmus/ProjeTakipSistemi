    private void finansEkleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_finansEkleActionPerformed
        // TODO add your handling code here:
        dtm2.getDataVector().removeAllElements();
        boolean malzemeVarmı = true;
        EntityManager em = emf.createEntityManager();

        String alısTarih;
        alısTarih = (alısGun.getSelectedIndex() + 1) + "," + (alısAy.getSelectedIndex() + 1) + "," + (alısYıl.getSelectedIndex() + 2018);
        Query q = em.createQuery("SELECT p FROM Proje p");
        List<Proje> projeler = q.getResultList();
        Query q2 = em.createQuery("select m from Malzeme m");
        List<Malzeme> malzemeler = q2.getResultList();

        for (Proje proje : projeler) {
            if (proje.getKullaniciid().equals(kullanıcıId)) {
                for (Malzeme malzeme : malzemeler) {
                    if (malzeme.getKullanilacagiprojekodu().equals(proje.getProjekodu())) {
                        dtm2.setColumnIdentifiers(new String[]{"Malzeme İsmi", "Miktar", "Türü", "Birim Fiyatı", "Nakliye Fiyatı", "Çalışacağı Proje", "Malzeme Kodu", "Tarih"});
                        dtm2.addRow(new String[]{malzeme.getAdi(), malzeme.getMiKtar(), malzeme.getCiNsi(), malzeme.getBiRiMfiYat(), malzeme.getNakliYefiYat(), malzeme.getKullanilacagiprojekodu(), malzeme.getKodu(), malzeme.getTariH()});
                    }
                }
            }
        }
        for (Proje proje : projeler) {
            if (proje.getKullaniciid().equals(kullanıcıId)) {
                for (Malzeme malzeme : malzemeler) {
                    if (malzeme.getKullanilacagiprojekodu().equals(proje.getProjekodu())) {
                        if (malzeme.getKodu().equals(malzemeKodu.getText())) {
                            malzemeVarmı = false;
                        }
                    }
                }
            }
        }
        if (!(malzemeKodu.getText().equals("") || miktar.getText().equals("") || malzemeİsmi.getText().equals("") || birimFiyatı.getText().equals("") || nakliyeFiyatı.getText().equals(""))) {
            if (malzemeVarmı) {
                if (!desenKontrol("[^0-9]", miktar.getText())) {
                    if (!desenKontrol("[^0-9]", birimFiyatı.getText())) {
                        if (!desenKontrol("[^0-9]", nakliyeFiyatı.getText())) {

                            Malzeme yeniMalzeme = new Malzeme(malzemeKodu.getText(), malzemeİsmi.getText(), (String) birimCinsi.getSelectedItem(), miktar.getText(), birimFiyatı.getText(), nakliyeFiyatı.getText(), alısTarih, (String) jComboBox2.getSelectedItem());
                            em.getTransaction().begin();
                            em.persist(yeniMalzeme);
                            em.getTransaction().commit();
                            dtm2.addRow(new String[]{yeniMalzeme.getAdi(), yeniMalzeme.getMiKtar(), yeniMalzeme.getCiNsi(), yeniMalzeme.getBiRiMfiYat(), yeniMalzeme.getNakliYefiYat(), yeniMalzeme.getKullanilacagiprojekodu(), yeniMalzeme.getKodu(), yeniMalzeme.getTariH()});
                            personelTablosu.setModel(dtm2);
                            JOptionPane.showMessageDialog(this, "Malzeme Oluşturuldu.");
                        } else {
                            JOptionPane.showMessageDialog(this, "Nakliye Fiyatına Sadece Rakam Girdiğinizden Emin Olun ...", "UYARI", JOptionPane.ERROR_MESSAGE, null);
                        }
                    } else {
                        JOptionPane.showMessageDialog(this, "Birim Fiyatına Sadece Rakam Girdiğinizden Emin Olun ...", "UYARI", JOptionPane.ERROR_MESSAGE, null);
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Miktara Sadece Rakam Girdiğinizden Emin Olun ...", "UYARI", JOptionPane.ERROR_MESSAGE, null);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Bu Koda Sahip Malzeme Vardır ...", "UYARI", JOptionPane.ERROR_MESSAGE, null);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Bütün Alanları Doldurunuz ...", "UYARI", JOptionPane.ERROR_MESSAGE, null);
        }
    }//GEN-LAST:event_finansEkleActionPerformed

