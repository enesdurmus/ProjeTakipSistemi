    public projeYönetim(String kullanıcıId) {
        try {
            con = DriverManager.getConnection("jdbc:derby://localhost:1527/Database", "enes", "123");
            initComponents();
            String tc;
            EntityManager em = emf.createEntityManager();
            this.kullanıcıId = kullanıcıId;
            dtm.getDataVector().removeAllElements();
            dtm.setColumnIdentifiers(new String[]{"Personel Adı", "Soyadı", "Özelliği", "Rütbesi", "Çalışacağı Proje", "Personel TC", "Giris Tarihi"});
            Statement s = con.createStatement();
            ResultSet rs = s.executeQuery("select * from PROJE");
            while (rs.next()) {
                if (rs.getString("KULLANICIID").equals(kullanıcıId)) {
                    cbm.addElement(rs.getString("PROJEKODU"));
                }
            }
            Query q = em.createQuery("select p from Personel p");
            Query q2 = em.createQuery("select p from Proje p");
            List<Personel> personeller = q.getResultList();
            List<Proje> projeler = q2.getResultList();
            for (Proje proje : projeler) {
                for (Personel personel : personeller) {
                    if (proje.getKullaniciid().equals(kullanıcıId)) {
                        if (proje.getProjekodu().equals(personel.getCalisacagiproje())) {
                            Personel a = new Personel();
                            tc = personel.getTckimlik();
                            a.setTckimlik(tc);
                            a.setCalisacagiproje(personel.getCalisacagiproje());
                            a.setMaas(personel.getMaas());
                            a.setCinsiyet(personel.getCinsiyet());
                            a.setAdi(personel.getAdi());
                            a.setOzelligi(personel.getOzelligi());
                            a.setRutbesi(personel.getRutbesi());
                            a.setSoyadi(personel.getSoyadi());
                            a.setGiristarih(personel.getGiristarih());
                            dtm.addRow(new String[]{a.getAdi(), a.getSoyadi(), a.getOzelligi(), a.getRutbesi(), a.getCalisacagiproje(), a.getTckimlik(), a.getGiristarih()});
                        }
                    }
                }
            }
            jComboBox2.setModel(cbm);
            jComboBox1.setModel(cbm);
            personelTablosu.setModel(dtm);
        } catch (SQLException ex) {
            Logger.getLogger(projeYönetim.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

